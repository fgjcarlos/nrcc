// TestRenderSettingsJS_AdminAuth_NoDefault tests adminAuth without default
func TestRenderSettingsJS_AdminAuth_NoDefault(t *testing.T) {
	cfg := model.DefaultFullAppConfig()
	cfg.Security.AdminAuth = &model.AdminAuthConfig{
		Type: "credentials",
		Users: []model.AdminAuthUser{
			{
				Username:    "admin",
				Password:    "$2a$10$hash",
				Permissions: "*",
			},
		},
		Default: nil,
	}

	output := RenderSettingsJS(cfg)

	// Check adminAuth block still present
	if !strings.Contains(output, `adminAuth: {`) {
		t.Error("Missing adminAuth block")
	}

	// Check default is NOT present
	lines := strings.Split(output, "\n")
	inAdminAuth := false
	defaultFound := false
	for _, line := range lines {
		if strings.Contains(line, "adminAuth: {") {
			inAdminAuth = true
		}
		if inAdminAuth && strings.Contains(line, "default: {") {
			defaultFound = true
		}
		if inAdminAuth && strings.Contains(line, "},") && !strings.Contains(line, "adminAuth") {
			inAdminAuth = false
		}
	}

	if defaultFound {
		t.Error("adminAuth default should not be rendered when nil")
	}
}

