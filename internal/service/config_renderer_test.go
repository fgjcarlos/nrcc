// TestRenderSettingsJS_HTTPS tests HTTPS configuration
func TestRenderSettingsJS_HTTPS(t *testing.T) {
	cfg := model.DefaultFullAppConfig()
	cfg.HTTPS.Enabled = true
	cfg.HTTPS.KeyFile = "/etc/ssl/key.pem"
	cfg.HTTPS.CertFile = "/etc/ssl/cert.pem"
	cfg.HTTPS.CAFile = "/etc/ssl/ca.pem"

	output := RenderSettingsJS(cfg)

	// Check fs require
	if !strings.Contains(output, "const fs = require('fs');") {
		t.Error("Missing fs require for HTTPS")
	}

	// Check key readFileSync
	if !strings.Contains(output, `key: fs.readFileSync("/etc/ssl/key.pem")`) {
		t.Error("Missing key readFileSync")
	}

	// Check cert readFileSync
	if !strings.Contains(output, `cert: fs.readFileSync("/etc/ssl/cert.pem")`) {
		t.Error("Missing cert readFileSync")
	}

	// Check CA readFileSync
	if !strings.Contains(output, `ca: fs.readFileSync("/etc/ssl/ca.pem")`) {
		t.Error("Missing ca readFileSync")
	}

	// Verify fs is only required once
	fsCount := strings.Count(output, "const fs = require('fs');")
	if fsCount != 1 {
		t.Errorf("fs require should appear exactly once, got %d", fsCount)
	}
}

